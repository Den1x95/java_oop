/**
 * P0231
 * Primjer osnovnih tipova podataka i literala
 */
public class P0231 {

    public static void main(String[] args) {

        byte b = 122;           // byte: 8bit
        short s = 356;		    // short: 16bit
        int i = 23442;		    // integer: 32bit, int literal
        long l = 4435653L;	    // long: 64bit, long literal

        float f = 6.4F;		    // float: 32bit, float literal
        double d = 4.532;	    // double: 64bit, double literal

        char c = 'c';		    // character: 16bit unicode, char literal
        boolean bool = false; 	// boolean: true ili false, boolean literal, rezervirane rijeci

    }
}